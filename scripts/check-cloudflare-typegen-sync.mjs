import { readFileSync, writeFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { join } from "node:path";

const root = process.cwd();

const cases = [
  {
    output: "worker/worker-configuration.d.ts",
    args: ["wrangler", "types", "worker/worker-configuration.d.ts", "-c", "worker/wrangler.toml", "-c", "backend/wrangler.jsonc"],
  },
  {
    output: "backend/worker-configuration.d.ts",
    args: ["wrangler", "types", "backend/worker-configuration.d.ts", "-c", "backend/wrangler.jsonc"],
  },
];

function normalizeGeneratedTypes(raw) {
  return raw.replace(/^\/\/ Generated by Wrangler by running `wrangler types .*?`\s*\(hash: .*?\)\n/m, "");
}

try {
  for (const item of cases) {
    const outputPath = join(root, item.output);
    const original = readFileSync(outputPath, "utf8");

    execFileSync("npx", item.args, {
      cwd: root,
      stdio: "pipe",
      encoding: "utf8",
    });

    const regenerated = readFileSync(outputPath, "utf8");
    writeFileSync(outputPath, original, "utf8");

    const expected = normalizeGeneratedTypes(original);
    const actual = normalizeGeneratedTypes(regenerated);

    if (expected !== actual) {
      console.error(`Cloudflare runtime types are out of sync: ${item.output}`);
      console.error("Run `npm run typegen:cf` and commit the regenerated files.");
      process.exit(1);
    }
  }

  console.log("Cloudflare runtime type generation is in sync.");
} finally {}
