import { readFileSync, writeFileSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { join } from "node:path";

const root = process.cwd();

const cases = [
  {
    output: "apps/edge/worker-configuration.d.ts",
    args: ["wrangler", "types", "apps/edge/worker-configuration.d.ts", "-c", "apps/edge/wrangler.toml", "-c", "apps/backend/wrangler.jsonc"],
  },
  {
    output: "apps/backend/worker-configuration.d.ts",
    args: ["wrangler", "types", "apps/backend/worker-configuration.d.ts", "-c", "apps/backend/wrangler.jsonc"],
  },
];

function normalizeGeneratedTypes(raw) {
  return raw.replace(/^\/\/ Generated by Wrangler by running `wrangler types .*?`\s*\(hash: .*?\)\n/m, "");
}

try {
  for (const item of cases) {
    const outputPath = join(root, item.output);
    const original = readFileSync(outputPath, "utf8");

    execFileSync("bunx", item.args, {
      cwd: root,
      stdio: "pipe",
      encoding: "utf8",
    });

    const regenerated = readFileSync(outputPath, "utf8");
    writeFileSync(outputPath, original, "utf8");

    const expected = normalizeGeneratedTypes(original);
    const actual = normalizeGeneratedTypes(regenerated);

    if (expected !== actual) {
      console.error(`Cloudflare runtime types are out of sync: ${item.output}`);
      console.error("Run `bun run typegen:cf` and commit the regenerated files.");
      process.exit(1);
    }
  }

  console.log("Cloudflare runtime type generation is in sync.");
} finally {}
